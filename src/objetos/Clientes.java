package objetos;

public class Clientes {

	String dni;
	String nombre;
	String apellido;
	String direccion;
	String localidad;
	
	public Clientes() {
		
	}
	
	public Clientes(String dni, String nombre, String apellido, String direccion, String localidad) {
		super();
		this.dni = dni;
		this.nombre = nombre;
		this.apellido = apellido;
		this.direccion = direccion;
		this.localidad = localidad;
	}
	
}

